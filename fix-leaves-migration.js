/**
 * Fix leaves and leave_settings migration
 * Looks up teacher by name to find the correct teacherId
 */
const admin = require('firebase-admin');
const { PrismaClient } = require('@prisma/client');
const fs = require('fs');
const path = require('path');

const serviceAccount = JSON.parse(
  fs.readFileSync(path.join(__dirname, 'serviceAccountKey.json'), 'utf-8')
);
if (!admin.apps.length) {
  admin.initializeApp({ credential: admin.credential.cert(serviceAccount) });
}

const firestoreDb = admin.firestore();
const prisma = new PrismaClient();

function ts(v) {
  if (!v) return null;
  if (v instanceof Date) return v;
  if (v?.toDate) return v.toDate();
  if (typeof v === 'string') return new Date(v);
  return null;
}
function str(v, fallback = '') {
  return v == null ? fallback : String(v);
}
function num(v, fallback = 0) {
  const n = parseFloat(v);
  return isNaN(n) ? fallback : n;
}

const LEAVE_TYPES = ['Casual', 'Sick', 'WeeklyHoliday', 'Other'];
function mapLeaveType(v) {
  if (!v) return 'Casual';
  const found = LEAVE_TYPES.find(x => x.toLowerCase() === v.toLowerCase());
  return found || 'Other';
}

async function main() {
  console.log('🔧 Fixing leaves & leave_settings...\n');

  // Load all teachers from PostgreSQL for name lookup
  const teachers = await prisma.teacher.findMany();
  console.log(`Found ${teachers.length} teachers in PostgreSQL:`);
  teachers.forEach(t => console.log(`  teacherId=${t.teacherId} name="${t.name}"`));
  console.log();

  // ── Fix Leaves ─────────────────────────────────────────────
  const leavesSnap = await firestoreDb.collection('leaves').get();
  const leaves = leavesSnap.docs.map(d => ({ _id: d.id, ...d.data() }));
  console.log(`📦 Firebase leaves: ${leaves.length}`);

  let ok = 0, skip = 0, err = 0;
  for (const d of leaves) {
    const teacherName = str(d.teacherName || d.teacher_name, '');

    // Try to find teacher by name (case-insensitive)
    const teacher = teachers.find(
      t => t.name.toLowerCase().trim() === teacherName.toLowerCase().trim()
    );

    if (!teacher) {
      console.error(`  ❌ No teacher found for name: "${teacherName}" (doc: ${d._id})`);
      err++;
      continue;
    }

    try {
      await prisma.leave.upsert({
        where: { id: d._id },
        update: {},
        create: {
          id: d._id,
          teacherId: teacher.teacherId,
          teacherName,
          startDate: str(d.startDate || d.start_date, ''),
          endDate: str(d.endDate || d.end_date, ''),
          days: num(d.days, 1),
          type: mapLeaveType(d.type),
          reason: d.reason || null,
          monthYear: str(d.monthYear || d.month_year, ''),
          createdAt: ts(d.createdAt) || new Date(),
        },
      });
      ok++;
    } catch (e) {
      console.error(`  ❌ ${d._id}: ${e.message}`);
      err++;
    }
  }
  console.log(`   ✅ ${ok} inserted, ⏭ ${skip} skipped, ❌ ${err} errors\n`);

  // ── Fix Leave Settings ──────────────────────────────────────
  const settingsSnap = await firestoreDb.collection('leave_settings').get();
  const settings = settingsSnap.docs.map(d => ({ _id: d.id, ...d.data() }));
  console.log(`📦 Firebase leave_settings: ${settings.length}`);

  ok = 0; skip = 0; err = 0;
  for (const d of settings) {
    const teacherName = str(d.teacherName || d.teacher_name, '');

    const teacher = teachers.find(
      t => t.name.toLowerCase().trim() === teacherName.toLowerCase().trim()
    );

    if (!teacher) {
      console.error(`  ❌ No teacher found for name: "${teacherName}" (doc: ${d._id})`);
      err++;
      continue;
    }

    try {
      await prisma.leaveSettings.upsert({
        where: { teacherId: teacher.teacherId },
        update: {},
        create: {
          id: d._id,
          teacherId: teacher.teacherId,
          teacherName,
          weeklyHolidays: d.weeklyHolidays || d.weekly_holidays || [],
          joinDate: str(d.joinDate || d.join_date, ''),
          lastAutoGeneratedDate: d.lastAutoGeneratedDate || d.last_auto_generated_date || null,
          createdAt: ts(d.createdAt) || new Date(),
        },
      });
      ok++;
    } catch (e) {
      console.error(`  ❌ ${d._id}: ${e.message}`);
      err++;
    }
  }
  console.log(`   ✅ ${ok} inserted, ⏭ ${skip} skipped, ❌ ${err} errors\n`);

  console.log('✅ Fix complete!');
}

main()
  .catch(e => { console.error('💥 Fatal:', e); process.exit(1); })
  .finally(async () => { await prisma.$disconnect(); process.exit(0); });
